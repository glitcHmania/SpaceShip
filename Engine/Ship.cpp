void Ship::ProcessMovement(const MainWindow& wnd, const Graphics& gfx_int, float deltaTime)
{
	if (wnd.kbd.KeyIsPressed(VK_UP))
	{
		y = y - v * deltaTime;
	}
	if (wnd.kbd.KeyIsPressed(VK_DOWN))
	{
		y = y + v * deltaTime;
	}
	if (wnd.kbd.KeyIsPressed(VK_RIGHT))
	{
		x = x + v * deltaTime;
	}
	if (wnd.kbd.KeyIsPressed(VK_LEFT))
	{
		x = x - v * deltaTime;
	}
}

void Ship::ClampScreen(const Graphics& gfx)
{
	if (y >= gfx.ScreenHeight - height - 10)
	{
		y = gfx.ScreenHeight - height - 10;
	}
	if (y <= 10)
	{
		y = 10;
	}
	if (x >= gfx.ScreenWidth - width - 10)
	{
		x = gfx.ScreenWidth - width - 10;
	}
	if (x <= 10)
	{
		x = 10;
	}
}

